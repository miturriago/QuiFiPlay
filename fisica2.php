	<center><h1 id="titulo">SI(sistema internacional)</h1></center>
    <p>El Sistema Internacional de Unidades, abreviado SI, también denominado Sistema Internacional de Medidas, es el heredero del antiguo sistema métrico decimal.
Una de las principales características del Sistema Internacional de Medidas, es que sus unidades están basadas en fenómenos físicos fundamentales. La única excepción a es que es la definición de la unidad de la magnitud Masa, el kilogramo, que está definida como la masa de un prototipo internacional del kilogramo que está almacenado en la Oficina Internacional de Pesos y Medidas, en Sèvres, Francia.
Las unidades del SI son la referencia internacional de las indicaciones de todos los instrumentos de medida, y a las que están referidas a través de una cadena ininterrumpida de calibraciones o comparaciones.
El Sistema Internacional de Unidades consta de siete unidades básicas, también denominadas unidades fundamentales, que definen a las correspondientes magnitudes físicas fundamentales, que han sido elegidas por convención, y que permiten expresar cualquier magnitud física en términos o como combinación de ellas. 
Estás siete unidades son:</br>
1.	Kilogramo</br>
2.	Segundo</br>
3.	Pascal</br>
4.	Joule</br>
5.	Amperio</br>
6.	Kelvin</br>
7.	Newton</br>
</P>
    <p>
    Un joule equivale a la cantidad de trabajo efectuado por una fuerza de 1 newton actuando a través de una distancia de 1 metro.
Un amperio es la unidad para la intensidad de corriente.</br>
El Pascal es unidad de medida que mide la presión. es la presión uniforme que, actuando sobre una superficie plana de 1 metro cuadrado.
</br>
El kelvin es la unidad fundamental de temperatura lleva su nombre en honor al científico William Thomson, Lord Kelvin. Se define al kelvin como la 1/273 parte de la temperatura termodinámica del punto triple del agua (que es el punto fijo en el que coexisten el hielo, el agua líquida y el vapor de agua en equilibrio).
</br>
Un newton es la fuerza que hay que ejercer sobre un kilogramo de masa para que adquiera una aceleración de un metro sobre segundo cuadrado.
</br>
El segundo es la unidad oficial de tiempo, para el SI. Hasta 1956 se definía en términos del día solar medio, dividido en 24 horas. Cada hora se divide en 60 minutos, y cada minuto en 60 segundos. Así, hay 86,400 segundos por día y el segundo se definía como la 1/86,400 parte del día solar medio.
</br>
