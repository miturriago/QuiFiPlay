	<center><h1 id="titulo">¿Qué es la física?</h1></center>
    <p>
La física es la ciencia que estudia el funcionamiento del universo, desde el movimiento de la materia por el espacio y el tiempo, hasta la energía y la fuerza.  
La física utiliza el método científico para descubrir los principios básicos que gobiernan la materia. La física existe para ayudarnos a darle sentido a lo que nos rodea.
<center><h1 id="titulo">¿Qué estudia la física?</h1></center>
El propósito de la física es describir el funcionamiento de todo a nuestro alrededor, desde el movimiento de partículas diminutas hasta el movimiento de las naves espaciales. Velocidad, movimiento, dirección y aceleración son términos comunes en física.
La física también explica los fenómenos luminosos y sonoros. La luz y el sonido son ondas con características particulares. Conceptos como calor, trabajo, fuerza y energía han sido desarrollados gracias al trabajo de importantes físicos.
La física contribuye directamente a muchas otras disciplinas. En arquitectura, la física forma parte de los estudios de acústica, iluminación, calentamiento y estabilidad estructural. La geología requiere de la física para el análisis de terremotos. La biofísica es un híbrido de la biología y la física.
En medicina, la física está presente en métodos de diagnóstico como los rayos X, la resonancia magnética y el ultrasonido. También lo está en tratamientos como la radioterapia.

