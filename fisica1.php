	<center><h1 id="titulo">Fenómenos físicos</h1></center>
    <p>Los fenómenos físicos son los cambios que sufre un cuerpo, materia o sustancia sin alterar su composición y ocurren cuando se lleva a cabo un proceso o cambio, que puede ser reversible, sin perder sus características, propiedades ni modificar su naturaleza.
    Reversible: Que puede volver a un estado o condición anterior.</P>
	<center><h1 id="titulo">Tipos de fenómenos físicos</h1></center>
    <p>
MOVIEMIENTO: 
Se produce por el desplazamiento de los cuerpos, en lo que se denomina fenómenos mecánicos. Tenemos como ejemplos: el vuelo de las aves, la caída de los cuerpos, el choque entre dos cuerpos
</br>
CALOR: 
Se origina en la agitación de las moléculas de un cuerpo, en lo que se llama fenómenos calóricos. Por ejemplo: para hervir agua, se necesita calor que agite las moléculas del agua hasta que estas se convierten en vapor.
</br>
SONIDO: 
La vibración de un cuerpo elástico produce el sonido. A este efecto se lo denomina fenómenos acústicos, que los estudia la Acústica. Por ejemplo, cuando tocas la guitarra vibran las cuerdas y se escucha el sondo. 
</br>
LUZ:
Es forma de energía que nos permite ver lo que nos rodea.
</br>
ELECTRICIDAD:
Es un conjunto de fenómenos producidos por el movimiento e interacción entre las cargas eléctricas positivas y negativas de los cuerpos físicos.
</br>
MAGNETISMO: 
Se define como la fuerza de atracción de imanes que presentan un polo positivo y otro negativo, conocido como dipolo.
</p>
